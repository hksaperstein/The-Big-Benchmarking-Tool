
function openNav() {
    document.getElementById("mySidenav").style.width = "250px";
    document.getElementById("main_text").style.marginRight = "250px"
}

/* Set the width of the side navigation to 0 */
function closeNav() {
    document.getElementById("mySidenav").style.width = "0";
    document.getElementById("main_text").style.marginRight = "0px"

}